package JavaBasics.Durgasoft.a1_Operators;

public class a2_Comparison {
    public static void main(String[] args) {
        int a=10, b=20;
        System.out.println("a==b : "+(a==b));   // false
        System.out.println("a!=b : "+(a!=b));   // true
        System.out.println("a<b : "+(a<b));     // true
        System.out.println("a>b : "+(a>b));     // false
        System.out.println("a<=b : "+(a<=b));   // true
        System.out.println("a>=b : "+(a>=b));   // false
    }
}
